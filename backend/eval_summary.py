#!/usr/bin/env python3
"""
Honest evaluation summary for the DWSIM Agentic AI project.

Reads whatever real artifacts exist (eval_log.json, hydrogen_results.jsonl,
benchmark results) and emits a thesis-ready RESULTS.md table — WITHOUT
inflating anything. Specifically it distinguishes:

  * "completed"  (the chat turn finished without an exception)
        from
    "verified-correct" (an independent judge / benchmark confirmed the answer)
  * mock-bridge runs from live-DWSIM runs
  * sample sizes are printed next to every average

so the reader can see exactly how strong (or weak) each number is. Re-run after
a live `/eval/benchmark/run-all` to refresh the headline pass-rate.

Usage:  python eval_summary.py            # prints summary + writes RESULTS.md
"""
from __future__ import annotations
import json
import os
import statistics as st
from typing import Any, Dict, List

_HERE = os.path.dirname(os.path.abspath(__file__))


def _load_json(name: str, default: Any) -> Any:
    p = os.path.join(_HERE, name)
    if not os.path.exists(p):
        return default
    try:
        with open(p, encoding="utf-8") as f:
            return json.load(f)
    except Exception:
        return default


def _load_jsonl(name: str) -> List[Dict]:
    p = os.path.join(_HERE, name)
    if not os.path.exists(p):
        return []
    out = []
    with open(p, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                try:
                    out.append(json.loads(line))
                except Exception:
                    pass
    return out


def _pct(num: int, den: int) -> str:
    return f"{num/den*100:.0f}% ({num}/{den})" if den else "n/a"


def summarize() -> str:
    L: List[str] = []
    def w(s=""): L.append(s)

    # ── Interaction sessions (eval_log.json) ─────────────────────────────────
    evlog    = _load_json("eval_log.json", {})
    sessions = evlog.get("sessions", []) if isinstance(evlog, dict) else []
    n = len(sessions)
    completed   = sum(1 for s in sessions if not s.get("error"))
    tool_using  = sum(1 for s in sessions if (s.get("tool_count") or 0) > 0)
    durs        = [s["duration_s"] for s in sessions if s.get("duration_s")]
    judged      = [s["judge_scores"] for s in sessions
                   if isinstance(s.get("judge_scores"), dict) and s["judge_scores"]]

    def judge_mean(k: str):
        vals = [j[k] for j in judged if isinstance(j.get(k), (int, float))]
        return (round(st.mean(vals), 2), len(vals)) if vals else (None, 0)

    # ── Hydrogen case study (hydrogen_results.jsonl) ─────────────────────────
    hydro     = _load_jsonl("hydrogen_results.jsonl")
    hy_mock   = sum(1 for r in hydro if r.get("mock") is True)
    hy_live   = sum(1 for r in hydro if r.get("mock") is False)
    hy_conv   = sum(1 for r in hydro if r.get("converged"))
    hy_yields = [r["h2_yield_mol_pct"] for r in hydro
                 if isinstance(r.get("h2_yield_mol_pct"), (int, float))]

    # ── Formal benchmark suite ───────────────────────────────────────────────
    bench_results = evlog.get("benchmark_results", []) if isinstance(evlog, dict) else []
    bench_n = len(bench_results)

    # ── Render ───────────────────────────────────────────────────────────────
    w("# DWSIM Agentic AI — Evaluation Summary")
    w()
    w("> Auto-generated by `eval_summary.py` from the repo's logged artifacts. "
      "Numbers are reported with sample sizes and a clear separation of "
      "*completed* vs *verified-correct* and *mock* vs *live DWSIM*. "
      "Re-run after a live benchmark to refresh the headline pass-rate.")
    w()
    w("## 1. Interaction sessions (`eval_log.json`)")
    w()
    w("| Metric | Value | What it means |")
    w("|---|---|---|")
    w(f"| Sessions logged | {n} | total recorded agent turns |")
    w(f"| Completed without error | {_pct(completed, n)} | the turn finished — "
      f"**not** a correctness measure |")
    w(f"| Sessions invoking >=1 tool | {_pct(tool_using, n)} | i.e. did real "
      f"flowsheet/sim work (rest are Q&A) |")
    if durs:
        w(f"| Turn duration | median {st.median(durs):.0f}s / mean "
          f"{st.mean(durs):.0f}s | latency |")
    w(f"| Sessions with LLM-judge scores | {len(judged)} | independent quality "
      f"check coverage |")
    if judged:
        for k in ("overall", "physical_plausibility", "property_package_correctness",
                  "completeness", "hallucination_absence"):
            m, c = judge_mean(k)
            if m is not None:
                w(f"| &nbsp;&nbsp;judge: {k} | {m}/5 (n={c}) | "
                  f"{'small sample — indicative only' if c < 10 else ''} |")
    w()
    w("## 2. Hydrogen (biogas-SMR) case study (`hydrogen_results.jsonl`)")
    w()
    w("| Metric | Value | Note |")
    w("|---|---|---|")
    w(f"| Total runs | {len(hydro)} | |")
    w(f"| Live-DWSIM runs | {hy_live} | **0 = no live-engine validation yet** |")
    w(f"| Mock-bridge runs | {hy_mock} | exercise the workflow, not DWSIM physics |")
    w(f"| Converged | {_pct(hy_conv, len(hydro))} | of all runs |")
    if hy_yields:
        w(f"| H2 yield (mock) | {st.mean(hy_yields):.1f} mol% mean, "
          f"range {min(hy_yields):.1f}-{max(hy_yields):.1f} | mock numbers |")
    w()
    w("## 3. Formal benchmark suite")
    w()
    if bench_n == 0:
        w("**Not yet run.** `eval_log.json[\"benchmark_results\"]` is empty — the "
          "25-task harness exists (`benchmark_tasks.py`) but no measured "
          "pass-rate has been recorded. This is the key missing artifact for an "
          "unqualified \"objective achieved\" claim. Run `/eval/benchmark/run-all` "
          "against a live DWSIM + LLM key to populate it.")
    else:
        passed = sum(1 for b in bench_results if b.get("passed"))
        w(f"Pass-rate: **{_pct(passed, bench_n)}** across {bench_n} tasks.")
    w()
    w("## 4. Component-level correctness (unit tests)")
    w()
    w("`pytest -q` from `backend/`: **465 passed, 1 skipped** at time of writing "
      "(mock bridge/agent; live-DWSIM tests auto-skip). This validates the "
      "software components, not end-to-end task success against real DWSIM.")
    w()
    w("## 5. Honest verdict")
    w()
    w("- **Built / integrated:** yes — a genuine multi-step agent + DWSIM bridge "
      "+ design + optimization stack, component-tested.")
    w("- **Validated end-to-end against live DWSIM:** **not yet evidenced** — all "
      "case-study runs are mock; the benchmark pass-rate is unrecorded; "
      "LLM-judge coverage is tiny (n=" + str(len(judged)) + ").")
    w("- **To close the gap:** one live `run-all` benchmark execution + re-run "
      "this script.")
    return "\n".join(L) + "\n"


def main() -> None:
    md = summarize()
    out = os.path.join(_HERE, "RESULTS.md")
    with open(out, "w", encoding="utf-8") as f:
        f.write(md)
    # ascii-safe console echo (Windows cp1252 consoles choke on unicode)
    print(md.encode("ascii", "replace").decode("ascii"))
    print(f"[eval_summary] wrote {out}")


if __name__ == "__main__":
    main()
